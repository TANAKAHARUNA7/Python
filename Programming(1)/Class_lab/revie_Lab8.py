#1つの英字を受け、その文字が（a、e、i、o、u）のいずれかであるかどうかを判別する
num = input("文字を入力してください")


if num == "a":
    print(num,"は母音です")
elif num == "e":
    print(num,"は母音です")
elif num == "i":
    print(num,"は母音です")
elif num == "o":
    print(num,"は母音です")
elif num == "u":
    print(num,"は母音です")   
else:
    print(num,"は母音ではありません")
    
       
       
       
       
       
       
       