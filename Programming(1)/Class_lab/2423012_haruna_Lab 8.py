# 사용자에게 하나의 문자를 입력받습니다.
alphabet = input("한 문자를 입력하세요: ")

# 입력받은 문자가 모음인지 아닌지 확인합니다.
# 결과를 출력합니다
if alphabet == "a":
    print(alphabet , "는 모음입니다.")
elif alphabet == "i":
    print(alphabet , "는 모음입니다.")
elif alphabet == "u":
    print(alphabet , "는 모음입니다.")
elif alphabet == "e":
    print(alphabet , "는 모음입니다.")   
elif alphabet == "o":
    print(alphabet , "는 모음입니다.")     
else :
    print(alphabet , "는 모임이 아닙니다.")






