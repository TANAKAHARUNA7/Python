package VariableAndConstant1;

public class Lab3 {
    public static void main(String[] args) {
        // 半径の値
        double radius = 5.0;
        // 円周率
        final double PI = 3.14159;
        // 円の面積計算
        double circle = PI * radius * radius ;

        System.out.printf("円の面積: %.2f%n",circle);

    }

}
