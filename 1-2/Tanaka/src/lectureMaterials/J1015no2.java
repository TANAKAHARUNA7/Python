package lectureMaterials;

public class J1015no2 {
    public static void main(String[] args) {
        // isLonggedIn が真の場合”歓迎します！”を出力して、
        // 偽の場合は“ログインに失敗しました”を出力。
        // ログイン成功可否によって歓迎メッセージまたはエラーメッセージを表示する
        boolean isLonggedIn = true; // ログイン成功　可否

        if (isLonggedIn) {
            System.out.println("歓迎します");
        }else{
            System.out.println("ログイン失敗");
        }





    }
}
