package lectureMaterials;

public class StringExample {
    public static void main(String[] args) {
        String greeting = "Hello, World!"; // 文字列　リテラル
        String name = "Haruna";
        String fullgreeting = greeting + " " + name; // 文字列連結

        System.out.println("あいさつ: " + greeting);
        System.out.println("名前: " + name);
        System.out.println("全体挨拶: " + fullgreeting);

    }
}
