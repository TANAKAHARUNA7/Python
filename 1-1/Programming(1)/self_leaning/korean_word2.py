# 出題済みの単語のインデックスを保存するリスト
check_list = []

# 全単語を出題するまでループ
while len(check_list) < sum(len(lst) for lst in kword_list):
    # ランダムにカテゴリを選択し、その中から単語を選ぶ
    category = random.choice([i for i in range(len(kword_list)) if i not in check_list or len(check_list) < len(kword_list[i])])
    word_index = random.choice([j for j in range(len(kword_list[category])) if (category, j) not in check_list])

    # 出題済みリストに追加
    check_list.append((category, word_index))

    print(f"문제: {kword_list[category][word_index]}")

    # 単語に合う漢字とよみがなを入力
    kanzi_input = input("한자를 입력하세요: ")
    yomigana_input = input("요미가나를 입력하세요: ")

    # 判断
    if kanzi_input == kanzi_list[category][word_index] and yomigana_input == yomigana_list[category][word_index]:
        print("정답！＾＾\n")
    else:
        print(f"땡 ㅠㅠ\n정답은: {kanzi_list[category][word_index]}, {yomigana_list[category][word_index]}\n")

